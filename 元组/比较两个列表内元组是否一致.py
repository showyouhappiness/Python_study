# 深度比较函数，递归比较列表内部元素
def deep_compare(obj1, obj2):
    if isinstance(obj1, list) and isinstance(obj2, list):
        if len(obj1) != len(obj2):
            return False
        return all(deep_compare(o1, o2) for o1, o2 in zip(obj1, obj2))
    elif isinstance(obj1, dict) and isinstance(obj2, dict):
        if len(obj1) != len(obj2):
            return False
        for key, value in obj1.items():
            if key not in obj2 or not deep_compare(value, obj2[key]):
                return False
        return True
    elif isinstance(obj1, str) and isinstance(obj2, str):
        return obj1 == obj2
    elif isinstance(obj1, (int, float)) and isinstance(obj2, (int, float)):
        return obj1 == obj2
    else:
        # 对于json，使用 json.dumps 将其转换为字符串后比较
        return json.dumps(obj1, sort_keys=True) == json.dumps(obj2, sort_keys=True)


